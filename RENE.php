			La criatura ganó este nombre oscuro, mística a partir de los informes iniciales de Puerto Rico en 1995. En marzo y agosto de 1995 , los ataques a más de 150 animales de granja dejaron sin habla funcionarios . Las declaraciones de testigos , publicados en los periódicos locales , hablaron de una criatura con un "cuerpo de reptil , cabeza ovalada , ojos saltones de color rojo , dientes colmillos y largo , como dardos lengua. " Se constató que los animales de granja en Puerto Rico para ser drenado de la sangre con heridas punzantes en el cuello. No hay carne fue tomado del cuerpo de los animales . La situación en Puerto Rico llegó a tal fiebre que el alcalde José Soto reclutó voluntarios para cazar la criatura semanal durante casi un año , sin éxito.
