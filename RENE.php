			It’s important at this point to make a distinction. When chupacabras are reported, they usually fall into one of two categories. First, and most common to the Puerto Rico incidents, is a chupacabra that is reptile-like in nature with leathery greenish-gray skin and spines running down the spine of the back. The monster can be approximately three to four feet tall and is a bipedal – standing and hopping like a kangaroo.
		</p>
