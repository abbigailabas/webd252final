			The second, increasingly common version of the chupacabra is more like a strange breed of wild dog or coyote. This version lacks the hair of a dog, but features the pronounced spinal ridge or “spikes” similar to the reptilian chupacabra. The four-legged, dog-like chupacabra is also known for some fearsome fangs and claws used for training animal’s blood. The “mark” of the chupacabra on the victims is typically one to three holes, and in the shape of an upside-down triangle when three holes are apparent.
